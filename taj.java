public class my_class{
    String name;
    int roll;

    my_class(String s, int n){
        name = s;
        roll = n;
    }

    public int getRoll(){
        return roll;
    }
    public int getName(){
        return name;
    }
    public void setRoll(int r){
        roll =r;
    }

    public void setName(String n){
        name =n;
    }

}

